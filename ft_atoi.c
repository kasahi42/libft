#include "libft.h"

int	ft_atoi(const char *s)
{
	int	tmp;
	int	check;

	while ((*s >= '\t' && *s <= '\r') || (*s == ' '))
		s ++;
	check = 1;
	if (*s == '-')
		check = -1;
	if (*s == '+' || *s == '-')
		s ++;
	tmp = 0;
	while (*s >= '0' && *s <= '9')
	{
		tmp = (tmp * 10) +((int)*s - '0');
		s ++;
	}
	return ((int)tmp * check);
}
